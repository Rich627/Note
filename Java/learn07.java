public class learn07 {
    public static void main(String[] arg){
        //2進制 0b開頭
        int num1 = 0b10;
        //8進制 0開頭
        int num2 = 023;
        //16進制 0~9 a~f  0x開頭
        int num3 = 0x15;
        
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
    }
}
