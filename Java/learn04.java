public class learn04 {
    public static void main(String[] args){
        //char 只能有一個字要用`''`
        char c1 = 'a';
        //用unicode
    
        char c2 = '\u0048';
        System.out.println(c2);
        //ASCII
        char c3 = 97;
        System.out.println(c3);

        boolean bo1 = true;
        boolean bo2 = false;
        System.out.println(bo1);
        //string 用`""`
        String s1 = "string";
        System.out.println(s1);
    }
}
